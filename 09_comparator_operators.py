# operadores de comparación

# >
print(7 > 3)#esto retorna un booleano afirmando o negando la premisa
print(3 > 7)
print(7 > 7)

# <
print(5 < 6)
print(6 < 5)
print(5 < 5)

# >=
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

# <=
print(2 <= 1)
print(2 <= 3)
print(2 <= 2)

# ==
print(2 == 1) #false
print(2 == 3) #false
print(2 == 2) #true

# !=
print(2 != 1) #true
print(2 != 3) #true
print(2 != 2) #false

# operaciones de comparación de string
print("Apple" == "Apple") #true
print("Apple" == "apple") #false
print("1" == 1) #false porque tienen tipos distintos de datos

# comparación variables
age = 18
print(age >= 18) #true
age = 12
print(age >= 18) #false
