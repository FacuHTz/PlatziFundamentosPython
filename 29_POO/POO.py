#La programación orientada a objetos consiste en trasladar la naturaleza de los objetos de la vida real a código de programación
#lod objetos de la realidad tienen caracteristicas(atributos y propiedades) y funcionalidades o comportamientos(funciónes o métodos)

#Ventajas:
#-Modularización(division en pequeñas partes) de un programa completo.
#-Código fuente muy reutilizable.
#-Código fuente más fácil de imcrementar en el futuro.
#-Si existe un fallo en ua pequeña parte del código el programa completo no debe fallar necesariamente. Además, es más fácil de corregir esos fallos.
#-Encapsulamiento: Ocultamiento del funcionamiento interno de un objeto