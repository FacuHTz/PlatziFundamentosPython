# distinguir distintos tipos de datos

# String
my_name = "Facu"
my_name = 'Facu'
type_string = type(my_name)
print(type_string, my_name)

# int
my_age = 25
type_int = type(my_age)
print(type_int, my_age)

# boolean
is_single = True
type_boolean = type(is_single)
print(type_boolean, is_single)

# inputs
my_profession = input("A que te dedicas: ")
type_input = type(my_profession)
print(type_input, my_profession)