#es una estructura repetitiva  que nos permite repetir varias iteraciones basandonos en el resultado de una expresion logica que puede tener como resultado una expresión booleana de True o False

indice = 1

while indice < 10:
  print(f"valor actual: {indice }")
  indice = indice + 1

print("acá termina")

inicio = 2

while inicio <= 100:
  print(f"Numero par: {inicio}")
