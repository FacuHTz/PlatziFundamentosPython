# declarando e inicializando una variable
my_name = "facu"
print(my_name)

my_age = 12
print(my_age)

# asignarle un nuevo valor a la misma variable
my_name = "bren"
# se puede usar una coma o un + para concatenar elementos, usando la coma se genera un espacio entremedio, usando el + no.
print("el nombre es: " + my_name)
print("el nombre es: ", my_name)

